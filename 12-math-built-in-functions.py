import math
x = 25
print('Power =', math.pow(x, 2))
print('Root =', math.sqrt(x))
print('PI =', math.pi)
print('Exponential =', math.e)
print('Factorial =', math.factorial(x))
print('Remainder =', math.remainder(x, 2))
print('Tau =', math.tau)

y = 25.5
print('Ceiling =', math.ceil(y))
print('Floor =', math.floor(y))

